from geopy.distance import geodesic

def calculate_distance(lat1, lon1, lat2, lon2):
    """Calculates the geodesic distance between two points in kilometers."""
    point1 = (lat1, lon1)
    point2 = (lat2, lon2)
    return geodesic(point1, point2).km

